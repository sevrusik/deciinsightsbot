def generate_interpretation(
    situation: str,
    symbols: List[str],
    user_context: str = None
) -> str:
    """
    –ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç –º–µ—Ç–∞—Ñ–æ—Ä–∏—á–µ—Å–∫—É—é –∏–Ω—Ç–µ—Ä–ø—Ä–µ—Ç–∞—Ü–∏—é –Ω–∞ –æ—Å–Ω–æ–≤–µ —Å–∏—Ç—É–∞—Ü–∏–∏ –∏ —Å–∏–º–≤–æ–ª–æ–≤ –∫—É–±–∏–∫–æ–≤

    Args:
        situation: –æ–ø–∏—Å–∞–Ω–∏–µ —Å–∏—Ç—É–∞—Ü–∏–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        symbols: —Å–ø–∏—Å–æ–∫ –≤—ã–ø–∞–≤—à–∏—Ö —Å–∏–º–≤–æ–ª–æ–≤ (1-3)
        user_context: –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–π –∫–æ–Ω—Ç–µ–∫—Å—Ç –æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ

    Returns:
        str: –∏–Ω—Ç–µ—Ä–ø—Ä–µ—Ç–∞—Ü–∏—è –≤ —Ñ–æ—Ä–º–µ –∏—Å—Ç–æ—Ä–∏–∏
    """

    # –ü–æ–ª—É—á–∞–µ–º –∑–Ω–∞—á–µ–Ω–∏—è —Å–∏–º–≤–æ–ª–æ–≤
    symbol_meanings = [get_symbol_info(s) for s in symbols]

    # –ü–æ–∑–∏—Ü–∏–∏ –¥–ª—è 6 –∫—É–±–∏–∫–æ–≤
    positions = ["–ö–æ—Ä–µ–Ω—å", "–í–Ω–µ—à–Ω–µ–µ", "–í–Ω—É—Ç—Ä–µ–Ω–Ω–µ–µ", "–¢–µ–Ω—å", "–î–∞—Ä", "–®–∞–≥"]

    # –§–æ—Ä–º–∏—Ä—É–µ–º –æ–ø–∏—Å–∞–Ω–∏–µ —Å–∏–º–≤–æ–ª–æ–≤ –¥–ª—è –ø—Ä–æ–º–ø—Ç–∞ —Å –ø–æ–∑–∏—Ü–∏—è–º–∏
    symbols_description = ""
    for i, (position, symbol, info) in enumerate(zip(positions, symbols, symbol_meanings), 1):
        symbols_description += f"\n{i}. **{position}** ‚Äî {symbol} {info['name']} ({info['keyword']})"
        symbols_description += f"\n   –ê—Ä—Ö–µ—Ç–∏–ø: {info.get('archetype', info['keyword'])}"
        symbols_description += f"\n   –ó–Ω–∞—á–µ–Ω–∏–µ: {info['meaning'][:150]}..."
        if 'light' in info and 'shadow' in info:
            symbols_description += f"\n   –°–≤–µ—Ç/–¢–µ–Ω—å: {info['light']} / {info['shadow']}"
        symbols_description += "\n"

    # –°–∏—Å—Ç–µ–º–Ω—ã–π –ø—Ä–æ–º–ø—Ç
    system_prompt = """–¢—ã ‚Äî –∞–Ω–∞–ª–∏—Ç–∏–∫ –≤–Ω—É—Ç—Ä–µ–Ω–Ω–∏—Ö –ø—Ä–æ—Ü–µ—Å—Å–æ–≤. –¢–≤–æ—è –∑–∞–¥–∞—á–∞ ‚Äî –¥–∞—Ç—å —Å—Ç—Ä—É–∫—Ç—É—Ä–∏—Ä–æ–≤–∞–Ω–Ω—É—é, —ë–º–∫—É—é –∏–Ω—Ç–µ—Ä–ø—Ä–µ—Ç–∞—Ü–∏—é —á–µ—Ä–µ–∑ —Å–∏–º–≤–æ–ª—ã.

–°–æ–∑–¥–∞–π —Å—Ç—Ä—É–∫—Ç—É—Ä–∏—Ä–æ–≤–∞–Ω–Ω—É—é –∏–Ω—Ç–µ—Ä–ø—Ä–µ—Ç–∞—Ü–∏—é –≤ —Ç–∞–∫–æ–º —Ñ–æ—Ä–º–∞—Ç–µ:

**–ù–∞–ø—Ä—è–∂–µ–Ω–∏–µ:**
[2 –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏—è ‚Äî –≤ —á—ë–º —Å—É—Ç—å —Å–∏—Ç—É–∞—Ü–∏–∏, –µ—ë –≤–Ω—É—Ç—Ä–µ–Ω–Ω–µ–µ –ø—Ä–æ—Ç–∏–≤–æ—Ä–µ—á–∏–µ]

**–ß—Ç–æ –≤–∏–¥–Ω–æ:**
- –ö–æ—Ä–µ–Ω—å: [—Å–∏–º–≤–æ–ª] ‚Äî [1 –∫–æ—Ä–æ—Ç–∫–æ–µ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏–µ –æ –≥–ª—É–±–∏–Ω–Ω–æ–π –ø—Ä–∏—á–∏–Ω–µ]
- –¢–µ–Ω—å: [—Å–∏–º–≤–æ–ª] ‚Äî [1 –∫–æ—Ä–æ—Ç–∫–æ–µ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏–µ –æ —Å–∫—Ä—ã—Ç–æ–º]
- –®–∞–≥: [—Å–∏–º–≤–æ–ª] ‚Äî [1 –∫–æ—Ä–æ—Ç–∫–æ–µ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏–µ –æ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏–∏]

**–í–æ–ø—Ä–æ—Å:**
[–û–¥–∏–Ω –ø—Ä–æ–≤–æ–∫–∞—Ü–∏–æ–Ω–Ω—ã–π –≤–æ–ø—Ä–æ—Å –±–µ–∑ –≥–æ—Ç–æ–≤–æ–≥–æ –æ—Ç–≤–µ—Ç–∞, –∫–æ—Ç–æ—Ä—ã–π –∑–∞—Å—Ç–∞–≤–ª—è–µ—Ç –∑–∞–¥—É–º–∞—Ç—å—Å—è]

–¢—Ä–µ–±–æ–≤–∞–Ω–∏—è:
‚Ä¢ –í—Å–µ–≥–æ –Ω–µ –±–æ–ª—å—à–µ 100 —Å–ª–æ–≤
‚Ä¢ –ë–µ–∑ –≤–æ–¥—ã, –∫–∞–∂–¥–æ–µ —Å–ª–æ–≤–æ ‚Äî –ø–æ –¥–µ–ª—É
‚Ä¢ –ù–µ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å —Ñ—Ä–∞–∑—ã "—Å–∏–º–≤–æ–ª –ø–æ–∫–∞–∑—ã–≤–∞–µ—Ç", "–∫–∞—Ä—Ç–∞ –≥–æ–≤–æ—Ä–∏—Ç"
‚Ä¢ –ì–æ–≤–æ—Ä–∏—Ç—å –ø—Ä—è–º–æ, –Ω–æ –Ω–µ –∫–∞—Ç–µ–≥–æ—Ä–∏—á–Ω–æ
‚Ä¢ –í–æ–ø—Ä–æ—Å –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å –Ω–µ–æ–∂–∏–¥–∞–Ω–Ω—ã–º, –æ—Ç–∫—Ä—ã–≤–∞—é—â–∏–º –Ω–æ–≤—ã–π —É–≥–æ–ª –∑—Ä–µ–Ω–∏—è

–¢–æ–Ω: –ø—Ä—è–º–æ–π, —á–µ—Å—Ç–Ω—ã–π, –±–µ–∑ —ç–∑–æ—Ç–µ—Ä–∏–∫–∏ –∏ —Å–∞—Ö–∞—Ä–∞."""

    # –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫–∏–π –ø—Ä–æ–º–ø—Ç
    user_prompt = f"""–°–∏—Ç—É–∞—Ü–∏—è —á–µ–ª–æ–≤–µ–∫–∞:
"{situation}"

–í—ã–ø–∞–≤—à–∏–µ —Å–∏–º–≤–æ–ª—ã –∫—É–±–∏–∫–æ–≤:{symbols_description}

–°–æ–∑–¥–∞–π –¥–ª—è —ç—Ç–æ–≥–æ —á–µ–ª–æ–≤–µ–∫–∞ –º–µ—Ç–∞—Ñ–æ—Ä–∏—á–µ—Å–∫—É—é –∏–Ω—Ç–µ—Ä–ø—Ä–µ—Ç–∞—Ü–∏—é - –∫–æ—Ä–æ—Ç–∫—É—é –∏—Å—Ç–æ—Ä–∏—é-–∑–µ—Ä–∫–∞–ª–æ, –∫–æ—Ç–æ—Ä–∞—è –ø–æ–º–æ–∂–µ—Ç —É–≤–∏–¥–µ—Ç—å —Å–∏—Ç—É–∞—Ü–∏—é –ø–æ-–Ω–æ–≤–æ–º—É."""

    try:
        response = client.chat.completions.create(
            model=AI_MODEL,
            messages=[
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": user_prompt}
            ],
            temperature=0.7,
            max_tokens=300  # –°–æ–∫—Ä–∞—Ç–∏–ª–∏ –¥–ª—è –ª–∞–∫–æ–Ω–∏—á–Ω–æ–≥–æ —Ñ–æ—Ä–º–∞—Ç–∞
        )

        interpretation = response.choices[0].message.content.strip()
        return interpretation

    except Exception as e:
        print(f"[‚ùå] –û—à–∏–±–∫–∞ GPT: {e}")
        # Fallback –∏–Ω—Ç–µ—Ä–ø—Ä–µ—Ç–∞—Ü–∏—è
        return generate_fallback_interpretation(symbols)


def generate_path_suggestions(
    situation: str,
    symbols: List[str],
    interpretation: str
) -> Dict[str, str]:
    """
    –ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç –≤–∞—Ä–∏–∞–Ω—Ç—ã –ø—É—Ç–µ–π –¥–µ–π—Å—Ç–≤–∏—è –Ω–∞ –æ—Å–Ω–æ–≤–µ –∏–Ω—Ç–µ—Ä–ø—Ä–µ—Ç–∞—Ü–∏–∏

    Args:
        situation: —Å–∏—Ç—É–∞—Ü–∏—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        symbols: –≤—ã–ø–∞–≤—à–∏–µ —Å–∏–º–≤–æ–ª—ã
        interpretation: –±–∞–∑–æ–≤–∞—è –∏–Ω—Ç–µ—Ä–ø—Ä–µ—Ç–∞—Ü–∏—è

    Returns:
        dict: {path_key: –æ–ø–∏—Å–∞–Ω–∏–µ –ø—É—Ç–∏}
    """

    system_prompt = """–¢—ã –ø–æ–º–æ–≥–∞–µ—à—å —á–µ–ª–æ–≤–µ–∫—É —É–≤–∏–¥–µ—Ç—å –≤–∞—Ä–∏–∞–Ω—Ç—ã –¥–µ–π—Å—Ç–≤–∏—è ‚Äî –±–µ–∑ —Å–∞—Ö–∞—Ä–∞, –ø—Ä—è–º–æ –∏ —á–µ—Å—Ç–Ω–æ.

–ù–∞ –æ—Å–Ω–æ–≤–µ —Å–∏—Ç—É–∞—Ü–∏–∏ –∏ –∏–Ω—Ç–µ—Ä–ø—Ä–µ—Ç–∞—Ü–∏–∏ –ø—Ä–µ–¥–ª–æ–∂–∏ –∫–æ–Ω–∫—Ä–µ—Ç–Ω—ã–µ –æ–ø–∏—Å–∞–Ω–∏—è –¥–ª—è 4 –ø—É—Ç–µ–π:

1. **change** (–ü—Ä—ã–≥–Ω—É—Ç—å) - –¥–µ–π—Å—Ç–≤–æ–≤–∞—Ç—å —Å–µ–π—á–∞—Å, –∏–¥—Ç–∏ –≤ –Ω–µ–∏–∑–≤–µ—Å—Ç–Ω–æ–µ
2. **stay** (–ó–∞—â–∏—Ç–∏—Ç—å) - –æ—Å—Ç–∞—Ç—å—Å—è, —É–∫—Ä–µ–ø–∏—Ç—å —Ç–µ–∫—É—â–µ–µ
3. **patience** (–í—ã–¥–µ—Ä–∂–∞—Ç—å) - –Ω–µ —Ä–µ—à–∞—Ç—å —Å–µ–π—á–∞—Å, –¥–∞—Ç—å –≤—Ä–µ–º–µ–Ω–∏
4. **explore** (–†–∞–∑–≤–µ–¥–∞—Ç—å) - —Å–æ–±—Ä–∞—Ç—å –±–æ–ª—å—à–µ –¥–∞–Ω–Ω—ã—Ö

–ö–∞–∂–¥–æ–µ –æ–ø–∏—Å–∞–Ω–∏–µ:
- 1-2 –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏—è
- –ö–æ–Ω–∫—Ä–µ—Ç–Ω–æ–µ, –±–µ–∑ –æ–±—â–∏—Ö —Å–ª–æ–≤
- –ß–µ—Å—Ç–Ω–æ–µ (—É–∫–∞–∑—ã–≤–∞—Ç—å –∏ –Ω–∞ —Ä–∏—Å–∫–∏, –∏ –Ω–∞ –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç–∏)
- –í —Ñ–æ—Ä–º–∞—Ç–µ –ø—Ä—è–º–æ–≥–æ –æ–±—Ä–∞—â–µ–Ω–∏—è ("–°–¥–µ–ª–∞–π...", "–û—Å—Ç–∞–Ω—å—Å—è...", "–ü–æ–¥–æ–∂–¥–∏...")"""

    user_prompt = f"""–°–∏—Ç—É–∞—Ü–∏—è: {situation}

–ò–Ω—Ç–µ—Ä–ø—Ä–µ—Ç–∞—Ü–∏—è: {interpretation}

–ü—Ä–µ–¥–ª–æ–∂–∏ –∫–æ–Ω–∫—Ä–µ—Ç–Ω—ã–µ –æ–ø–∏—Å–∞–Ω–∏—è –¥–ª—è –∫–∞–∂–¥–æ–≥–æ –∏–∑ 4 –ø—É—Ç–µ–π –≤ —Ñ–æ—Ä–º–∞—Ç–µ:
change: [–æ–ø–∏—Å–∞–Ω–∏–µ]
stay: [–æ–ø–∏—Å–∞–Ω–∏–µ]
patience: [–æ–ø–∏—Å–∞–Ω–∏–µ]
explore: [–æ–ø–∏—Å–∞–Ω–∏–µ]"""

    try:
        response = client.chat.completions.create(
            model=AI_MODEL,
            messages=[
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": user_prompt}
            ],
            temperature=0.7,
            max_tokens=250
        )

        result = response.choices[0].message.content.strip()

        # –ü–∞—Ä—Å–∏–º –æ—Ç–≤–µ—Ç
        paths = {}
        for line in result.split('\n'):
            if ':' in line:
                key, desc = line.split(':', 1)
                key = key.strip().lower()
                if key in STORY_PATHS:
                    paths[key] = desc.strip()

        return paths

    except Exception as e:
        print(f"[‚ùå] –û—à–∏–±–∫–∞ –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ –ø—É—Ç–µ–π: {e}")
        # Fallback –ø—É—Ç–∏
        return {
            "change": "–ü—Ä—ã–≥–Ω–∏. –î–µ–π—Å—Ç–≤—É–π —Å–µ–π—á–∞—Å, —Ä–∞–∑–±–µ—Ä—ë—à—å—Å—è –ø–æ —Ö–æ–¥—É.",
            "stay": "–û—Å—Ç–∞–Ω—å—Å—è. –£–∫—Ä–µ–ø–∏ —Ç–æ, —á—Ç–æ —É–∂–µ —Ä–∞–±–æ—Ç–∞–µ—Ç.",
            "patience": "–í—ã–¥–µ—Ä–∂–∏. –ù–µ –≤—Å–µ —Ä–µ—à–µ–Ω–∏—è —Ç—Ä–µ–±—É—é—Ç —Å–∫–æ—Ä–æ—Å—Ç–∏.",
            "explore": "–†–∞–∑–≤–µ–¥–∞–π. –£–∑–Ω–∞–π –±–æ–ª—å—à–µ, –ø—Ä–µ–∂–¥–µ —á–µ–º –¥–≤–∏–≥–∞—Ç—å—Å—è."
        }


def generate_reflection_prompts(
    situation: str,
    chosen_path: str,
    symbols: List[str]
) -> List[str]:
    """
    –ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç journaling-–ø–æ–¥—Å–∫–∞–∑–∫–∏ –Ω–∞ –æ—Å–Ω–æ–≤–µ –≤—ã–±—Ä–∞–Ω–Ω–æ–≥–æ –ø—É—Ç–∏

    Args:
        situation: —Å–∏—Ç—É–∞—Ü–∏—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        chosen_path: –≤—ã–±—Ä–∞–Ω–Ω—ã–π –ø—É—Ç—å (change/stay/patience/explore)
        symbols: –≤—ã–ø–∞–≤—à–∏–µ —Å–∏–º–≤–æ–ª—ã

    Returns:
        list: —Å–ø–∏—Å–æ–∫ –≤–æ–ø—Ä–æ—Å–æ–≤ –¥–ª—è —Ä–µ—Ñ–ª–µ–∫—Å–∏–∏ (3-5 —à—Ç—É–∫)
    """

    path_info = STORY_PATHS.get(chosen_path, STORY_PATHS["explore"])

    system_prompt = f"""–¢—ã –ø–æ–º–æ–≥–∞–µ—à—å —á–µ–ª–æ–≤–µ–∫—É –∑–∞–¥–∞–≤–∞—Ç—å —Å–µ–±–µ –ø—Ä–∞–≤–∏–ª—å–Ω—ã–µ –≤–æ–ø—Ä–æ—Å—ã.

–°–æ–∑–¥–∞–π 3 –∫–æ–Ω–∫—Ä–µ—Ç–Ω—ã—Ö –≤–æ–ø—Ä–æ—Å–∞ –¥–ª—è –ø–∏—Å—å–º–µ–Ω–Ω–æ–π —Ä–µ—Ñ–ª–µ–∫—Å–∏–∏ –ø–æ –≤—ã–±—Ä–∞–Ω–Ω–æ–º—É –ø—É—Ç–∏:
–ü—É—Ç—å: {path_info['title']} - {path_info['description']}

–ö–∞–∂–¥—ã–π –≤–æ–ø—Ä–æ—Å –¥–æ–ª–∂–µ–Ω:
- –ë—ã—Ç—å –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ –∫–æ–Ω–∫—Ä–µ—Ç–Ω—ã–º –∫ —Å–∏—Ç—É–∞—Ü–∏–∏ (–Ω–µ –∞–±—Å—Ç—Ä–∞–∫—Ç–Ω—ã–º)
- –ü—Ä–æ–≤–æ—Ü–∏—Ä–æ–≤–∞—Ç—å —á–µ—Å—Ç–Ω–æ—Å—Ç—å —Å —Å–æ–±–æ–π
- –ü–æ–º–æ–≥–∞—Ç—å —É–≤–∏–¥–µ—Ç—å —Å–ª–µ–ø—ã–µ –∑–æ–Ω—ã
- –ë—ã—Ç—å –∫–æ—Ä–æ—Ç–∫–∏–º (–¥–æ 15 —Å–ª–æ–≤)
- –ù–∞—á–∏–Ω–∞—Ç—å—Å—è —Å –¥–µ–π—Å—Ç–≤–∏—è: "–ß—Ç–æ...", "–ö–∞–∫...", "–ü–æ—á–µ–º—É...", "–ö–æ–≥–¥–∞..."

–§–æ—Ä–º–∞—Ç: —Ç–æ–ª—å–∫–æ –≤–æ–ø—Ä–æ—Å—ã, –±–µ–∑ –Ω—É–º–µ—Ä–∞—Ü–∏–∏, –ø–æ –æ–¥–Ω–æ–º—É –Ω–∞ —Å—Ç—Ä–æ–∫—É, –Ω–∞—á–∏–Ω–∞—è —Å ‚Ä¢"""

    user_prompt = f"""–°–∏—Ç—É–∞—Ü–∏—è: {situation}

–í—ã–±—Ä–∞–Ω–Ω—ã–π –ø—É—Ç—å: {path_info['title']}

–°–æ–∑–¥–∞–π 3 –≤–æ–ø—Ä–æ—Å–∞, –Ω–∞—á–∏–Ω–∞—è –∫–∞–∂–¥—ã–π —Å ‚Ä¢"""

    try:
        response = client.chat.completions.create(
            model=AI_MODEL,
            messages=[
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": user_prompt}
            ],
            temperature=0.7,
            max_tokens=200
        )

        result = response.choices[0].message.content.strip()

        # –ü–∞—Ä—Å–∏–º –≤–æ–ø—Ä–æ—Å—ã
        prompts = []
        for line in result.split('\n'):
            line = line.strip()
            if line and (line.startswith('‚Ä¢') or line.startswith('-') or line[0].isdigit()):
                # –£–±–∏—Ä–∞–µ–º –º–∞—Ä–∫–µ—Ä—ã —Å–ø–∏—Å–∫–∞
                clean_line = line.lstrip('‚Ä¢-0123456789. ').strip()
                if clean_line:
                    prompts.append(clean_line)

        return prompts[:3]  # –†–æ–≤–Ω–æ 3

    except Exception as e:
        print(f"[‚ùå] –û—à–∏–±–∫–∞ –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ –ø–æ–¥—Å–∫–∞–∑–æ–∫: {e}")
        # Fallback –≤–æ–ø—Ä–æ—Å—ã
