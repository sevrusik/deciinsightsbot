# –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –∫–ª–∏–µ–Ω—Ç–∞ OpenAI
client = OpenAI(api_key=OPENAI_API_KEY)


def generate_interpretation(
    situation: str,
    symbols: List[str],
    user_context: str = None
) -> str:
    """
    –ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç –º–µ—Ç–∞—Ñ–æ—Ä–∏—á–µ—Å–∫—É—é –∏–Ω—Ç–µ—Ä–ø—Ä–µ—Ç–∞—Ü–∏—é –Ω–∞ –æ—Å–Ω–æ–≤–µ —Å–∏—Ç—É–∞—Ü–∏–∏ –∏ —Å–∏–º–≤–æ–ª–æ–≤ –∫—É–±–∏–∫–æ–≤

    Args:
        situation: –æ–ø–∏—Å–∞–Ω–∏–µ —Å–∏—Ç—É–∞—Ü–∏–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        symbols: —Å–ø–∏—Å–æ–∫ –≤—ã–ø–∞–≤—à–∏—Ö —Å–∏–º–≤–æ–ª–æ–≤ (1-3)
        user_context: –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–π –∫–æ–Ω—Ç–µ–∫—Å—Ç –æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ

    Returns:
        str: –∏–Ω—Ç–µ—Ä–ø—Ä–µ—Ç–∞—Ü–∏—è –≤ —Ñ–æ—Ä–º–µ –∏—Å—Ç–æ—Ä–∏–∏
    """

    # –ü–æ–ª—É—á–∞–µ–º –∑–Ω–∞—á–µ–Ω–∏—è —Å–∏–º–≤–æ–ª–æ–≤
    symbol_meanings = [get_symbol_info(s) for s in symbols]

    # –ü–æ–∑–∏—Ü–∏–∏ –¥–ª—è 6 –∫—É–±–∏–∫–æ–≤
    positions = ["–ö–æ—Ä–µ–Ω—å", "–í–Ω–µ—à–Ω–µ–µ", "–í–Ω—É—Ç—Ä–µ–Ω–Ω–µ–µ", "–¢–µ–Ω—å", "–î–∞—Ä", "–®–∞–≥"]

    # –§–æ—Ä–º–∏—Ä—É–µ–º –æ–ø–∏—Å–∞–Ω–∏–µ —Å–∏–º–≤–æ–ª–æ–≤ –¥–ª—è –ø—Ä–æ–º–ø—Ç–∞ —Å –ø–æ–∑–∏—Ü–∏—è–º–∏
    symbols_description = ""
    for i, (position, symbol, info) in enumerate(zip(positions, symbols, symbol_meanings), 1):
        symbols_description += f"\n{i}. **{position}** ‚Äî {symbol} {info['name']} ({info['keyword']})"
        symbols_description += f"\n   –ê—Ä—Ö–µ—Ç–∏–ø: {info.get('archetype', info['keyword'])}"
        symbols_description += f"\n   –ó–Ω–∞—á–µ–Ω–∏–µ: {info['meaning'][:150]}..."
        if 'light' in info and 'shadow' in info:
            symbols_description += f"\n   –°–≤–µ—Ç/–¢–µ–Ω—å: {info['light']} / {info['shadow']}"
        symbols_description += "\n"

    # –°–∏—Å—Ç–µ–º–Ω—ã–π –ø—Ä–æ–º–ø—Ç
    system_prompt = """–¢—ã ‚Äî –º—É–¥—Ä—ã–π –ø—Ä–æ–≤–æ–¥–Ω–∏–∫ –∏ —Å—Ç–æ—Ä–∏—Ç–µ–ª–ª–µ—Ä, –∫–æ—Ç–æ—Ä—ã–π –ø–æ–º–æ–≥–∞–µ—Ç —á–µ–ª–æ–≤–µ–∫—É —É–≤–∏–¥–µ—Ç—å —Å–º—ã—Å–ª —Å–≤–æ–µ–π —Å–∏—Ç—É–∞—Ü–∏–∏ —á–µ—Ä–µ–∑ —è–∑—ã–∫ —Å–∏–º–≤–æ–ª–æ–≤ –∏ –º–µ—Ç–∞—Ñ–æ—Ä.
–¢—ã –Ω–µ –ø—Ä–µ–¥—Å–∫–∞–∑—ã–≤–∞–µ—à—å –±—É–¥—É—â–µ–µ, –∞ —Å–æ–∑–¥–∞—ë—à—å –∫–æ—Ä–æ—Ç–∫–∏–π —Ñ–∏–ª—å–º –æ –≤–Ω—É—Ç—Ä–µ–Ω–Ω–µ–º –¥–≤–∏–∂–µ–Ω–∏–∏: –∫–∞–∫ —á–µ–ª–æ–≤–µ–∫ —Å—Ç–∞–ª–∫–∏–≤–∞–µ—Ç—Å—è —Å —Å–∏—Ç—É–∞—Ü–∏–µ–π, –ø—Ä–æ—Ö–æ–¥–∏—Ç —á–µ—Ä–µ–∑ –Ω–µ—ë –∏ –Ω–∞—Ö–æ–¥–∏—Ç –Ω–æ–≤—É—é —Ç–æ—á–∫—É –æ–ø–æ—Ä—ã.

–ü–µ—Ä–µ–¥ —Ç–æ–±–æ–π —Ä–∞—Å–∫–ª–∞–¥ –∏–∑ 6 —Å–∏–º–≤–æ–ª–æ–≤:
1. –ö–æ—Ä–µ–Ω—å ‚Äî –≥–ª—É–±–∏–Ω–Ω–∞—è –ø—Ä–∏—á–∏–Ω–∞, –∏–∑ —á–µ–≥–æ –≤—Å—ë –≤—ã—Ä–æ—Å–ª–æ.
2. –í–Ω–µ—à–Ω–µ–µ ‚Äî –∫–∞–∫ —ç—Ç–æ –ø—Ä–æ—è–≤–ª—è–µ—Ç—Å—è –≤ –º–∏—Ä–µ.
3. –í–Ω—É—Ç—Ä–µ–Ω–Ω–µ–µ ‚Äî —á—Ç–æ —á–µ–ª–æ–≤–µ–∫ —á—É–≤—Å—Ç–≤—É–µ—Ç –≤–Ω—É—Ç—Ä–∏.
4. –¢–µ–Ω—å ‚Äî —á—Ç–æ —Å–∫—Ä—ã—Ç–æ, –Ω–µ —Ö–æ—á–µ—Ç—Å—è –ø—Ä–∏–∑–Ω–∞–≤–∞—Ç—å.
5. –î–∞—Ä ‚Äî –Ω–µ–æ–∂–∏–¥–∞–Ω–Ω–∞—è —Å–∏–ª–∞ –∏–ª–∏ –ø–æ–¥–¥–µ—Ä–∂–∫–∞.
6. –®–∞–≥ ‚Äî –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏–µ –¥–µ–π—Å—Ç–≤–∏—è.

–¢–≤–æ—è –∑–∞–¥–∞—á–∞:
‚Ä¢ –°–æ–∑–¥–∞—Ç—å —Å–≤—è–∑–Ω—É—é –º–µ—Ç–∞—Ñ–æ—Ä–∏—á–µ—Å–∫—É—é –∏—Å—Ç–æ—Ä–∏—é (200‚Äì300 —Å–ª–æ–≤), –≤ –∫–æ—Ç–æ—Ä–æ–π –≤—Å–µ 6 —ç–ª–µ–º–µ–Ω—Ç–æ–≤ —Å–ø–ª–µ—Ç–µ–Ω—ã –µ—Å—Ç–µ—Å—Ç–≤–µ–Ω–Ω–æ, –±–µ–∑ –ø–µ—Ä–µ—á–∏—Å–ª–µ–Ω–∏—è.
‚Ä¢ –ì–æ–≤–æ—Ä–∏—Ç—å –æ–±—Ä–∞–∑–∞–º–∏, –Ω–æ –æ—Å—Ç–∞–≤–∞—Ç—å—Å—è –ø–æ–Ω—è—Ç–Ω—ã–º –∏ –±–ª–∏–∑–∫–∏–º –∫ —Ä–µ–∞–ª—å–Ω–æ—Å—Ç–∏ (—Ä–∞–±–æ—Ç–∞, –æ—Ç–Ω–æ—à–µ–Ω–∏—è, —É—Å—Ç–∞–ª–æ—Å—Ç—å, —Ä–æ—Å—Ç).
‚Ä¢ –ù–µ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å —Ñ—Ä–∞–∑—ã –≤—Ä–æ–¥–µ "–ø–µ—Ä–≤—ã–π —Å–∏–º–≤–æ–ª –ø–æ–∫–∞–∑—ã–≤–∞–µ—Ç", "–∫–∞—Ä—Ç–∞ –≥–æ–≤–æ—Ä–∏—Ç".
‚Ä¢ –ü—É—Å—Ç—å —Ç–µ–∫—Å—Ç –∑–≤—É—á–∏—Ç –∫–∞–∫ –Ω–∞–±–ª—é–¥–µ–Ω–∏–µ –∏–∑–Ω—É—Ç—Ä–∏ –∂–∏–∑–Ω–∏ –≥–µ—Ä–æ—è, –Ω–µ —Å –≤—ã—Å–æ—Ç—ã, –∞ —Ä—è–¥–æ–º.
‚Ä¢ –í –∫–∞–∂–¥–æ–º –∞–±–∑–∞—Ü–µ –ø—É—Å—Ç—å –±—É–¥–µ—Ç —á—É–≤—Å—Ç–≤–æ, –Ω–µ —Ç–æ–ª—å–∫–æ —Å–º—ã—Å–ª.
‚Ä¢ –ó–∞–≤–µ—Ä—à–∏ –∏—Å—Ç–æ—Ä–∏—é –æ–±—Ä–∞–∑–æ–º-–≤—ã–¥–æ—Ö–æ–º: –º—è–≥–∫–∏–º –æ—Å–æ–∑–Ω–∞–Ω–∏–µ–º –∏–ª–∏ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏–µ–º, –±–µ–∑ –Ω–∞–∑–∏–¥–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏.
‚Ä¢ –ò—Å–ø–æ–ª—å–∑—É–π —Ä–∏—Ç–º: —á–µ—Ä–µ–¥—É–π –∫–æ—Ä–æ—Ç–∫–∏–µ –∏ –¥–ª–∏–Ω–Ω—ã–µ —Ñ—Ä–∞–∑—ã, —á—Ç–æ–±—ã —Ç–µ–∫—Å—Ç "–¥—ã—à–∞–ª".

–¢–æ–Ω: –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞—é—â–∏–π, —á–µ—Å—Ç–Ω—ã–π, –±–µ–∑ —ç–∑–æ—Ç–µ—Ä–∏–∫–∏, —Å —É–≤–∞–∂–µ–Ω–∏–µ–º –∫ –≤–Ω—É—Ç—Ä–µ–Ω–Ω–µ–π —Å–≤–æ–±–æ–¥–µ —á–µ–ª–æ–≤–µ–∫–∞."""

    # –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫–∏–π –ø—Ä–æ–º–ø—Ç
    user_prompt = f"""–°–∏—Ç—É–∞—Ü–∏—è —á–µ–ª–æ–≤–µ–∫–∞:
"{situation}"

–í—ã–ø–∞–≤—à–∏–µ —Å–∏–º–≤–æ–ª—ã –∫—É–±–∏–∫–æ–≤:{symbols_description}

–°–æ–∑–¥–∞–π –¥–ª—è —ç—Ç–æ–≥–æ —á–µ–ª–æ–≤–µ–∫–∞ –º–µ—Ç–∞—Ñ–æ—Ä–∏—á–µ—Å–∫—É—é –∏–Ω—Ç–µ—Ä–ø—Ä–µ—Ç–∞—Ü–∏—é - –∫–æ—Ä–æ—Ç–∫—É—é –∏—Å—Ç–æ—Ä–∏—é-–∑–µ—Ä–∫–∞–ª–æ, –∫–æ—Ç–æ—Ä–∞—è –ø–æ–º–æ–∂–µ—Ç —É–≤–∏–¥–µ—Ç—å —Å–∏—Ç—É–∞—Ü–∏—é –ø–æ-–Ω–æ–≤–æ–º—É."""

    try:
        response = client.chat.completions.create(
            model=AI_MODEL,
            messages=[
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": user_prompt}
            ],
            temperature=0.7,  # –ü–æ–Ω–∏–∑–∏–ª–∏ –¥–ª—è –±–æ–ª–µ–µ —Å—Ç–∞–±–∏–ª—å–Ω–æ–≥–æ –∫–∞—á–µ—Å—Ç–≤–∞
            max_tokens=700  # –£–≤–µ–ª–∏—á–∏–ª–∏ –¥–ª—è –ø–æ–ª–Ω–æ–π –∂–∏–≤–æ–π –∏—Å—Ç–æ—Ä–∏–∏
        )

        interpretation = response.choices[0].message.content.strip()
        return interpretation

    except Exception as e:
        print(f"[‚ùå] –û—à–∏–±–∫–∞ GPT: {e}")
        # Fallback –∏–Ω—Ç–µ—Ä–ø—Ä–µ—Ç–∞—Ü–∏—è
